public class Usuarios {
    private String usu_id;
    private String usu_email;
    private String usu_password;
    private int usu_year;
    private int usu_genero;
    private String usu_facultad;
    private int usu_tipo;
    private int usu_sector;
    private String usu_actividades;

    public String getUsu_id() {
        return usu_id;
    }

    public void setUsu_id(String usu_id) {
        this.usu_id = usu_id;
    }

    public String getUsu_email() {
        return usu_email;
    }

    public void setUsu_email(String usu_email) {
        this.usu_email = usu_email;
    }

    public String getUsu_password() {
        return usu_password;
    }

    public void setUsu_password(String usu_password) {
        this.usu_password = usu_password;
    }

    public int getUsu_year() {
        return usu_year;
    }

    public void setUsu_year(int usu_year) {
        this.usu_year = usu_year;
    }

    public int getUsu_genero() {
        return usu_genero;
    }

    public void setUsu_genero(int usu_genero) {
        this.usu_genero = usu_genero;
    }

    public String getUsu_facultad() {
        return usu_facultad;
    }

    public void setUsu_facultad(String usu_facultad) {
        this.usu_facultad = usu_facultad;
    }

    public int getUsu_tipo() {
        return usu_tipo;
    }

    public void setUsu_tipo(int usu_tipo) {
        this.usu_tipo = usu_tipo;
    }

    public int getUsu_sector() {
        return usu_sector;
    }

    public void setUsu_sector(int usu_sector) {
        this.usu_sector = usu_sector;
    }

    public String getUsu_actividades() {
        return usu_actividades;
    }

    public void setUsu_actividades(String usu_actividades) {
        this.usu_actividades = usu_actividades;
    }
}
